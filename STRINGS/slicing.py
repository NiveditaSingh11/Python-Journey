name="Nivedita"

# N i v e d i t a
#follows zero based indexing

print(name[-4:])   # dita
print(name[:-4])   # Nive
print(name[::-1])  # atideviN
print(name[-5:-1]) # edit
print(name[0:8:2])
#sequence [start: stop: step]
'''
start → Index where slicing begins (inclusive)
stop → Index where slicing ends (exclusive)
step → How many positions to move each time
'''
print(name[-1:-9:-1])